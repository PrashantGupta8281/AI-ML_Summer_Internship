# --- HUGGING FACE MODEL LOADER ---
@st.cache_resource
def load_model_from_hf(repo_id):
    # Try common Keras model filenames from the repository
    possible_filenames = ["model.h5", "binary_image_classifier.h5", "gender_classifier.h5"]

    for filename in possible_filenames:
        try:
            model_path = hf_hub_download(repo_id=repo_id, filename=filename)
            model = tf.keras.models.load_model(model_path)
            return model, filename
        except Exception:
            continue

    # If standard names fail, attempt download assuming a default model.h5
    try:
        model_path = hf_hub_download(repo_id=repo_id, filename="model.h5")
        return tf.keras.models.load_model(model_path), "model.h5"
    except Exception as e:
        st.error(f"Failed to load model from Hugging Face repository '{repo_id}'. Ensure the repository contains a valid `.h5` file.")
        return None, None

# Load the model directly from Hugging Face
with st.spinner("Fetching pre-trained CNN model layers from Hugging Face Hub..."):
    model, deployed_filename = load_model_from_hf(REPO_ID)

