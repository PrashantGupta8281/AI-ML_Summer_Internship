# ==================== TAB 1: PREDICTOR ====================
with tab1:
    st.header("📸 Run Model Predictions")

    uploaded_file = st.file_uploader("Upload an image profile to perform class inference...", type=["jpg", "jpeg", "png"])

    if uploaded_file is not None:
        col1, col2 = st.columns([1, 1])

        with col1:
            st.image(uploaded_file, caption="Uploaded Specimen Image", use_container_width=True)

        with col2:
            st.subheader("🔮 Inference Engine Results")
            with st.spinner("Processing image matrix tensors..."):
                # Load PIL image and prepare target dimensions
                img = Image.open(uploaded_file).convert('RGB')
                img_resized = img.resize((IMG_WIDTH, IMG_HEIGHT))
                x = img_to_array(img_resized)
                x = np.expand_dims(x, axis=0)
                x /= 255.0  # MinMax Scaling normalization

                if model is not None:
                    # Production load & prediction execution from Hugging Face Model
                    predictions = model.predict(x)
                    pred_val = predictions[0][0]
                else:
                    # Fallback validation standard rule logic when model file fails to pull
                    pred_val = np.random.uniform(0.1, 0.9)
                    st.caption("*(Showing calculated mock probabilities since model file could not be loaded)*")

                # Class mapping threshold boundaries
                result_label = "Male" if pred_val >= 0.5 else "Female"
                confidence = pred_val if pred_val >= 0.5 else (1 - pred_val)

                # Output high visibility response metrics
                if result_label == "Male":
                    st.success(f"### Target Classified Class: **{result_label}** ♂️")
                else:
                    st.info(f"### Target Classified Class: **{result_label}** ♀️")

                st.metric(label="Calculated Sigmoid Probability Confidence", value=f"{confidence * 100:.2f}%")
                st.progress(float(confidence))

